/**
 * 주소 문자열에서 시도 코드를 추출한다.
 */
function detectProvince(address: string): string | null {
  for (const [code, keywords] of PROVINCE_KEYWORDS) {
    if (keywords.some(kw => address.includes(kw))) return code;
  }
  return null;
}

/**
 * 주소 문자열에서 LAWD_CD(5자리)를 추출한다.
 *
 * 전략:
 * 1. 시도가 인식되면 해당 시도 내에서 시군구 매칭
 * 2. 시도가 없으면 전국 시군구에서 가장 긴 매칭 우선
 */
export function extractLawdCd(address: string): string | null {
  const provCode = detectProvince(address);

  if (provCode) {
    const districts = REGION_MAP[provCode] ?? {};
    // 긴 이름(구체적) 우선 매칭
    const matches = Object.entries(districts)
      .filter(([name]) => address.includes(name))
      .sort((a, b) => b[0].length - a[0].length);
    if (matches.length > 0) return matches[0][1];
  }

  // 시도 미인식 → 전국 모든 시군구에서 매칭 (긴 이름 우선)
  const allDistricts = Object.values(REGION_MAP).flatMap(d => Object.entries(d));
  const matches = allDistricts
    .filter(([name]) => address.includes(name))
    .sort((a, b) => b[0].length - a[0].length);

  return matches.length > 0 ? matches[0][1] : null;
}

/**
 * LAWD_CD → 시군구 이름 (표시용)
 */
export function getLawdName(lawdCd: string): string {
  for (const districts of Object.values(REGION_MAP)) {
    for (const [name, code] of Object.entries(districts)) {
      if (code === lawdCd && !name.includes(' ')) return name;
    }
  }
  return lawdCd;
}

